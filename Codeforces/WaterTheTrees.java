    static  public long countdays(int arr[],long maxh){
        long one=0,two=0;
        long ans=Integer.MAX_VALUE;
        for(int it:arr){
            long temp=maxh-it;
            two+=temp/2;
            one+=temp%2;
        }

        if(one>two)
            ans=2*one-1;
        else{
            long temp2=two-one;
            if(temp2%3==0 || temp2%3==1)
                ans=2*(two-temp2/3);
            else
                ans=2*(two-temp2/3)-1;
        }
    return ans;
    }


