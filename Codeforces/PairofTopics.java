    public static int binser(int c[],int i){
        int low=i+1,high=c.length-1;
        int n=c.length;
        int count=n;
        while(low<=high){
            int mid=low+(high-low)/2;
            if(c[mid]+c[i]>0){
                count=mid;
                high=mid-1;
            }
            else
                low=mid+1;
        }
        return n-count;
    }
