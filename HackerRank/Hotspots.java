    static boolean check(int R, int r, int x, int y,
                            int rad)
    {
        double val = Math.sqrt(Math.pow(x, 2) + Math.pow(y, 2));
        if (val + rad <= R && val - rad >= R - r)
            return true;
        else
            return false;
    }
